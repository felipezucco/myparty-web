interface Props extends DialogInterface { }

const OrganizationForm: FC<Props> = ({ onClose, status, setStatus }) => {

  const { register, handleSubmit, setValue, getValues } = useForm<OrganizationDTO>();
  const [sending, setSending] = useState(false);
  const [emails, setEmails] = useState<String[]>([])
  const ctx = useContext(AuthContext);

  async function handleSubmitForm() {
    console.log('asd', getValues());
  }

  async function getEmail(data: string) {
    await getUserByEmail(data).then(res => setEmails(res.data));
  }

  return (
    <Dialog open={status}>
      <form onSubmit={handleSubmit(handleSubmitForm)} method={'POST'}>
        <DialogTitle>
          Criar Organização
          <IconButton onClick={() => setStatus(false)}>
            <Close />
          </IconButton>
        </DialogTitle>
        <DialogContent>
          <TextField variant="outlined"
            {...register('name')}
            label='Nome'
            margin={'normal'}
            required
          />
          <TextField type={'hidden'} {...register('username')} value={ctx.user?.username} />
          <Autocomplete
            multiple
            options={emails}
            {...register('organizers')}
            getOptionLabel={(option) => option.toString()}
            // defaultValue={[top100Films[13]]}
            loading={true}
            onChange={(event, value) => setValue('organizers', value)}
            filterSelectedOptions={true}
            renderInput={(params) => (
              <TextField
                {...params}
                onChange={(e) => getEmail(e.target.value)}
                margin={'normal'}
                variant={'outlined'}
                label="Organizadores"
              />
            )}
          />
        </DialogContent>
        <DialogActions>
          <LoadingButton
            type="submit"
            loading={sending}
            variant={"contained"}
            disableElevation>
            Criar Organização
          </LoadingButton>
        </DialogActions>
      </form>
    </Dialog>
  )
}

